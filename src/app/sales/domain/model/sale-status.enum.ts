/**
 * Represents the status of the sale.
 */
export enum SaleStatus {

  /**
   * Representation of a pending sale.
   */
  PENDING = 'PENDING',

  /**
   * Representation of a completed sale.
   */
  COMPLETED = 'COMPLETED',

  /**
   * Representation of a cancelled sale.
   */
  CANCELLED = 'CANCELLED',
}
